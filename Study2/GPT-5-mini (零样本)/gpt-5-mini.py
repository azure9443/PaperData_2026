def devdove_chat_json(prompt: str, max_retry: int = 10) -> str:
    last_err = None
    for attempt in range(max_retry):
        try:
            _sleep_min_interval()

            resp = devdove_client.chat.completions.create(
                model=MODEL,
                messages=[
                    {"role": "system", "content": "你是评分程序，只输出 JSON。"},
                    {"role": "user", "content": prompt},
                ],
                temperature=0,
                max_tokens=1800,
                # 如果这个参数不被网关支持，会报 400；你之前能跑说明大概率没问题
                response_format={"type": "json_object"},
            )

            # ——关键：做健壮性检查，避免 choices 为空时报 list index out of range
            if not getattr(resp, "choices", None) or len(resp.choices) == 0:
                raise RuntimeError("empty choices")

            msg = resp.choices[0].message
            content = getattr(msg, "content", None)
            if not content or not str(content).strip():
                raise RuntimeError("empty content")

            # ——关键：确保是 JSON（有时网关会塞点前后缀）
            content = str(content).strip()
            # 兜底：截取第一个 { 到最后一个 } 之间
            if "{" in content and "}" in content:
                content = content[content.find("{"):content.rfind("}")+1]

            # 验证可解析
            json.loads(content)
            return content

        except Exception as e:
            last_err = e

            # 1) 识别常见 429 “Please retry in Xs”
            m = re.search(r"Please retry in ([0-9.]+)s", str(e))
            if m:
                time.sleep(float(m.group(1)) + 0.8 + random.random())
                continue

            # 2) 若是 empty choices/empty content，通常是瞬时异常：稍微等久一点再重试
            if "empty choices" in str(e) or "empty content" in str(e):
                time.sleep(2.0 + random.random() * 1.5)
                continue

            # 3) 其它错误：指数退避
            time.sleep(min(30, (2 ** min(attempt, 6)) + random.random()))

    raise RuntimeError(f"多次重试仍失败：{last_err}")

