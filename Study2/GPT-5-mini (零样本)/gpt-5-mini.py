def parse_batch_json(text: str, n: int) -> List[int]:
    data = json.loads(text)
    scores = [None] * n
    for r in data["results"]:
        i = int(r["i"]); s = int(r["score"])
        if 0 <= i < n and 1 <= s <= 5:
            scores[i] = s
    if any(v is None for v in scores):
        raise ValueError("JSON 解析失败：results 未覆盖全部条目或分数越界")
    return [int(x) for x in scores]


