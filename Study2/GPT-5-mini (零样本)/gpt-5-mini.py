import json, time, random, re
from typing import List

# 你说可以慢一点：这里控制请求频率
MIN_INTERVAL = 1.2
_last_call_t = 0.0

def _sleep_min_interval():
    global _last_call_t
    now = time.monotonic()
    dt = now - _last_call_t
    if dt < MIN_INTERVAL:
        time.sleep((MIN_INTERVAL - dt) + random.random() * 0.2)
    _last_call_t = time.monotonic()

def _extract_json_obj(s: str) -> str:
    s = (s or "").strip()
    if "{" in s and "}" in s:
        s = s[s.find("{"): s.rfind("}") + 1]
    return s

def _client_with_timeout(timeout_s: float):
    # openai>=1.x 支持 with_options；不支持就退回原 client
    try:
        return devdove_client.with_options(timeout=timeout_s)
    except Exception:
        return devdove_client

def call_json(prompt: str, max_retry: int = 8, timeout_s: float = 60.0) -> str:
    """
    返回：一个可 json.loads 的 JSON 字符串
    """
    last_err = None
    cli = _client_with_timeout(timeout_s)

    for attempt in range(max_retry):
        try:
            _sleep_min_interval()
            resp = cli.chat.completions.create(
                model=MODEL,
                messages=[
                    {"role": "system", "content": "你是评分程序，只输出JSON，不要解释。"},
                    {"role": "user", "content": prompt},
                ],
                temperature=0,
                max_tokens=1800,
            )

            choices = getattr(resp, "choices", None) or []
            if len(choices) == 0:
                raise RuntimeError("empty choices")

            content = getattr(choices[0].message, "content", "")
            content = _extract_json_obj(content)
            json.loads(content)  # 验证
            return content

        except Exception as e:
            last_err = e
            # 429 常见提示：Please retry in Xs
            m = re.search(r"Please retry in ([0-9.]+)s", str(e))
            if m:
                time.sleep(float(m.group(1)) + 0.8 + random.random())
            else:
                time.sleep(min(15, (2 ** min(attempt, 5)) + random.random()))

    raise RuntimeError(f"call_json 重试失败：{last_err}")

def score_one(text: str) -> int:
    prompt = (
        RUBRIC.strip()
        + "\n\n【现在请评分这一条】\n"
        + f"微博内容：{text}\n"
        + "只输出JSON：{\"score\": 1}\n"
    )
    raw = call_json(prompt)
    d = json.loads(raw)
    s = int(d["score"])
    if not (1 <= s <= 5):
        raise ValueError("score out of range")
    return s

def score_batch(texts: List[str]) -> List[int]:
    n = len(texts)
    numbered = "\n".join([f"{i}. {t}" for i, t in enumerate(texts)])
    prompt = (
        RUBRIC.strip()
        + "\n\n【批量评分】\n"
        + "对每条给出 score(1~5)，并保留序号 i。\n"
        + "只输出严格JSON：{\"results\":[{\"i\":0,\"score\":1},{\"i\":1,\"score\":3}]}\n\n"
        + numbered
    )

    try:
        raw = call_json(prompt)
        obj = json.loads(raw)
        results = obj["results"]

        scores = [None] * n
        for r in results:
            i = int(r["i"]); s = int(r["score"])
            if 0 <= i < n and 1 <= s <= 5:
                scores[i] = s

        if any(v is None for v in scores):
            raise ValueError("batch results 不完整")

        return [int(x) for x in scores]

    except Exception:
        # 批量失败就逐条（慢一点但稳）
        return [score_one(t) for t in texts]

